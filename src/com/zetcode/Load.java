    protected void loadPlayerLocation(){
        FileReader playerLocation = null;
        String temp = new String();
        try{
            playerLocation = new FileReader("res/txt/playerLocation.txt");
            data.setIndex(0);
            while(true){
                temp = Integer.toString(playerLocation.read());
                if(!(temp == " ")) {
                    if (!(temp == "\n")) {
                        data.setValue(Integer.parseInt(temp));
                        data.setX();
                        data.setIndex(data.getIndex() + 1);
                    }
                    else {
                        data.setValue(Integer.parseInt(temp));
                        data.setY();
                        data.setIndex(data.getIndex()+1);
                    }
                }
            }
        }
        catch (IOException e){
            System.out.println("입출력 오류");
        }
    }
